const ROWS = PIXEL_ART.length
const COLS = PIXEL_ART[0].length
const POLE_COLS = 2

function FlagGrid({ includePole }: { includePole: boolean }) {
  const startCol = includePole ? 0 : POLE_COLS
  const endCol = includePole ? COLS : COLS - 1 // skip trailing transparent col on mobile
  const startRow = includePole ? 0 : 1 // skip pole-top transparent row on mobile

  const columns: number[][] = []
  for (let c = startCol; c < endCol; c++) {
    const col: number[] = []
    for (let r = startRow; r < ROWS; r++) {
      col.push(PIXEL_ART[r][c])
    }
    columns.push(col)
  }

  return (
    <>
      {columns.map((col, colIdx) => {
        const actualCol = colIdx + startCol
        const isPole = includePole && actualCol < POLE_COLS
        return (
          <div
            key={colIdx}
            className={isPole ? undefined : "flag-wave-col"}
            style={
              isPole
                ? undefined
                : ({ "--col": actualCol - POLE_COLS } as React.CSSProperties)
            }
          >
            {col.map((colorIdx, rowIdx) => (
              <div
                key={rowIdx}
                className="size-[6px] sm:size-[8px]"
                style={{ backgroundColor: PALETTE[colorIdx] }}
              />
            ))}
          </div>
        )
      })}
    </>
  )
}

export function PixelPirateFlag() {
  return (
    <>
      {/* Mobile: no pole, centered */}
      <div className="flex justify-center sm:hidden" aria-hidden="true">
        <div className="flex items-start">
          <FlagGrid includePole={false} />
        </div>
      </div>
      {/* Desktop: with pole, left-aligned */}
      <div className="hidden sm:flex items-start" aria-hidden="true">
        <FlagGrid includePole={true} />
      </div>
    </>
  )
}
